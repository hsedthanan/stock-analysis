def sum_two_numbers(a, b):
    result = a + b
    print(f"The sum of {a} and {b} is {result}")

def sum_five_numbers(a, b, c, d, e):
    result = a + b + c + d + e
    print(f"The sum of {a}, {b}, {c}, {d}, and {e} is {result}")

# Example usage
sum_two_numbers(10, 20)
sum_five_numbers(10, 20, 30, 40, 50)